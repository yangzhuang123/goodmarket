<script>
	export default {
		data() {
			return {
				id: '',
				baseUrl: '',
				ro:{
					dianpumingcheng : false,
					shangpinmingcheng : false,
					shangpinfenlei : false,
					tupian : false,
					pinpai : false,
					guige : false,
					shangpincanshu : false,
					shangpinjieshao : false,
					onelimittimes : false,
					alllimittimes : false,
					clicktime : false,
					clicknum : false,
					discussnum : false,
					totalscore : false,
					price : false,
					onshelves : false,
					storeupnum : false,
				},
				type: '',
				userTableName: localStorage.getItem('UserTableName'),
				ruleForm: {
					dianpumingcheng: '',
					shangpinmingcheng: '',
					shangpinfenlei: '',
					tupian: '',
					pinpai: '',
					guige: '',
					shangpincanshu: '',
					shangpinjieshao: '',
					onelimittimes: Number('-1') ,
					alllimittimes: Number('-1') ,
					clicktime: '',
					clicknum: '',
					discussnum: '',
					totalscore: '',
					price: '',
					onshelves: '',
					storeupnum: '',
				},
				shangpinfenleiOptions: [],
				pinpaiOptions: [],


				rules: {
					dianpumingcheng: [
					],
					shangpinmingcheng: [
						{ required: true, message: '商品名称不能为空', trigger: 'blur' },
					],
					shangpinfenlei: [
						{ required: true, message: '商品分类不能为空', trigger: 'blur' },
					],
					tupian: [
					],
					pinpai: [
						{ required: true, message: '品牌不能为空', trigger: 'blur' },
					],
					guige: [
					],
					shangpincanshu: [
					],
					shangpinjieshao: [
					],
					onelimittimes: [
						{ validator: this.$validate.isIntNumer, trigger: 'blur' },
					],
					alllimittimes: [
						{ validator: this.$validate.isIntNumer, trigger: 'blur' },
					],
					clicktime: [
					],
					clicknum: [
						{ validator: this.$validate.isIntNumer, trigger: 'blur' },
					],
					discussnum: [
						{ validator: this.$validate.isIntNumer, trigger: 'blur' },
					],
					totalscore: [
						{ validator: this.$validate.isNumber, trigger: 'blur' },
					],
					price: [
						{ required: true, message: '价格不能为空', trigger: 'blur' },
						{ validator: this.$validate.isNumber, trigger: 'blur' },
					],
					onshelves: [
						{ validator: this.$validate.isIntNumer, trigger: 'blur' },
					],
					storeupnum: [
						{ validator: this.$validate.isIntNumer, trigger: 'blur' },
					],
